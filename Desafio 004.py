x = input('Digite algo:')

print('O tipo primitivo é:', type(x))
print('A variável só tem espaços', x.isspace())
print('A variável é numérica:', x.isnumeric())
print('A variável é alfabético:', x.isalpha())
print('A variável é alfanumérica:', x.isalnum())
print('A variável é toda maiúscula:', x.isupper())
print('A variável é toda minúscula:', x.islower())
print('A variável está capitalizada:', x.istitle())



